    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if ($request->picture_name == null){return back();}
        $picture = new Picture();
        $picture->user_name = Auth::user()->name;
        $picture->product_id = $request->product_id;
        $picture->picture_name = $request->file('picture_name')->getClientOriginalName();
        
        $file = $request->picture_name;
        $upload_path = public_path() . '/img';
        $file->move($upload_path, $picture->picture_name);
        $picture->save();
        return redirect("product/$picture->product_id");
    }

